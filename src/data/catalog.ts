export function text(value: { en: string; zh: string }, locale: Locale) {
  return value[locale] || value.en;
}

export function formatCurrency(amount: number) {
  return new Intl.NumberFormat("en-MY", {
    style: "currency",
    currency: "MYR",
    maximumFractionDigits: 2,
  }).format(amount);
}

export function getCategory(id: string) {
  return categories.find((category) => category.id === id || category.slug === id);
}

export function getCategoryName(id: string, locale: Locale) {
  const category = getCategory(id);
  return category ? text(category.name, locale) : id;
}

export function getProductCategorySlug(product: Product) {
  const current = product.categorySlug || product.categoryId;
  if (categories.some((category) => category.slug === current)) return current;

  const target = `${product.name.en} ${product.name.zh} ${product.description.en} ${product.description.zh}`.toLowerCase();
  const hasAny = (keywords: string[]) => keywords.some((keyword) => target.includes(keyword.toLowerCase()));

  if (current === "juice-drinks") return "fresh-juice";
  if (current === "promotion") return "special-promotion-sales";
  if (current === "sauces-seasoning") return "seasonings-sauces";
  if (current === "frozen-food" && hasAny(["beef", "steak", "牛"])) return "beef";
  if (current === "japanese-products" && hasAny(["fillet", "slice", "saku", "片"])) return "fish-fillet";
  if (current === "japanese-products" && hasAny(["fish", "salmon", "shishamo", "鱼"])) return "whole-fish";
  if (current === "japanese-products") return "japanese";
  if (current === "seafood" && hasAny(["prawn", "shrimp", "虾"])) return "prawn";
  if (current === "seafood" && hasAny(["squid", "octopus", "鱿"])) return "squid";
  if (current === "seafood" && hasAny(["crab", "蟹"])) return "crab";
  if (current === "seafood" && hasAny(["scallop", "hotate", "带子"])) return "scallop";
  if (current === "seafood" && hasAny(["clam", "scallop", "mussel", "shell", "crab", "贝"])) return "shell";
  if (current === "seafood" && hasAny(["fillet", "slice", "saku", "片"])) return "fish-fillet";
  if (current === "seafood" && hasAny(["tempura", "fried", "nugget", "fries", "炸"])) return "others";
  if (current === "seafood") return "whole-fish";

  return current;
}

